class TemperatureScales2 {
public:
	double convert(int, int, int, int, int);
};

double TemperatureScales2::convert(int f1, int b1, int f2, int b2, int t) {
	double l = (double) ((double) (t - f1) / (b1 - f1)) * (b2 - f2);
	double r = l + f2;
	return r;
}


