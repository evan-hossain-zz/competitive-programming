class SemiPerfectSquare {
public:
	string check(int);
};

string SemiPerfectSquare::check(int N) {
	int i, j, k;
	for(i = 1; i <= N; i++)
    {
        for(j = i + 1; j <= N; j++)
        {
            if(i * j * j == N)
                return "Yes";
        }
    }
    return "No";
}


