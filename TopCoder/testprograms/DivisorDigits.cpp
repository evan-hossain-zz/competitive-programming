class DivisorDigits {
public:
	int howMany(int);
};

int DivisorDigits::howMany(int number)
{
    int arr[10], i, cnt = number;
	for(i = 1; i < 10; i++)
        arr[i] = 0;
    do
    {
        arr[cnt % 10]++;
        cnt /= 10;
    }
    while(cnt);
    cnt = 0;
    for(i = 1; i < 10; i++)
    {
        if(arr[i])
        {
            if(number % i == 0)
                cnt += arr[i];
        }
    }
    return cnt;
}


