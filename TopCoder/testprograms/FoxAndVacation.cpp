class FoxAndVacation {
public:
	int maxCities(int, vector <int>);
};

int FoxAndVacation::maxCities(int total, vector <int> d) {
	int i = 0;
	sort(d.begin(), d.end());
	while(d[i] <= total && i < d.size())
    {
        total-=d[i];
        i++;
    }
    return i;
}


