int dp[1010];

using namespace std;

class TheNumberGameDivTwo {
public:
	string find(int);
};

int call(int num)
{
    int &ret = dp[num];
    if(ret != -1)
        return ret;
    ret = 0;
    for(int i = 2; i < num; i++)
    {
        if(num % i == 0)
        {
            if(!call(num - i))
                ret = 1;
        }
    }
    return ret;
}

string TheNumberGameDivTwo::find(int n) {
    memset(dp, -1, sizeof dp);
    if(call(n))
        return "John";
    return "Brus";
}


