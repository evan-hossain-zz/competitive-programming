class ThePalindrome {
public:
	int find(string);
};

int ThePalindrome::find(string s) {
	int i, j, k;
	for(i = 0; i < s.size(); i++)
    {
        for(j = i, k = s.size() - 1; j <= k; j++, k--)
        {
            if(s[j] != s[k])
                break;
        }
        if(j > k)
            return s.size() + i;
    }
}


