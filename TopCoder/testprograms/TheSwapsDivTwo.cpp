class TheSwapsDivTwo {
public:
	int find(vector <int>);
};

int TheSwapsDivTwo::find(vector <int> s) {
    int i, j;
    long long cnt = 1, rep = 0;
    for(i = 0; i < s.size(); i++)
    {
        for(j = i + 1; j < s.size(); j++)
        {
            if(s[i] == s[j])
                rep++;
            cnt++;
        }
    }
    cout << cnt << endl;
    rep = max(rep, (long long) 1);
    cout << rep << endl;
    return cnt - rep;
}


