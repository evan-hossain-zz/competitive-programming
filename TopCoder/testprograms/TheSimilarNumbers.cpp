class TheSimilarNumbers {
public:
	int find(int, int);
};

int TheSimilarNumbers::find(int lower, int upper) {
	int cnt = 1, i, a, b, j;
//	cout << mid << endl;
	for(a = upper, b = upper - 1; b >= lower; b--)
    {
        if(a <= b * 10 && b <= a * 10 && a != b)
            continue;
        else if(a != b)
        {
            cnt++;
            a = b;
        }
    }
    return cnt;
}


double test0() {
	int p0 = 1;
	int p1 = 10;
	TheSimilarNumbers * obj = new TheSimilarNumbers();
	clock_t start = clock();
	int my_answer = obj->find(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
