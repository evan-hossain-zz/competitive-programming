class TrafficCongestionDivTwo {
public:
	long long theMinCars(int);
};

long long TrafficCongestionDivTwo::theMinCars(int t) {
    long long ans = 0;
	for(int i = t; i > 0; i -= 2)
        ans += (1LL << (i - 1));
    if(t % 2)
        return ans;
    return ans + 1;
}


