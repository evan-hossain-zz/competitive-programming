class LittleElephantAndBallsAgain {
public:
	int getNumber(string S) {
		int arr[S.size() + 5];
		arr[0] = 1;
		int i, mx = 1;
		for(i = 1; i < S.size(); i++)
        {
            if(S[i] == S[i - 1])
                arr[i] = arr[i - 1] + 1;
            else
                arr[i] = 1;
            mx = max(mx, arr[i]);
        }
        return S.size() - mx;
	}
};


